void bootloader_jumpto_userApp(void)
{
	
	void (*app_reset_handler)(void);  //function pointer to execute user_app
	
	uint32_t msp_val = *(volatile uint32_t*)0x08008000U;
	
	__set_MSP(msp_val); //set user flash address
	
	uint32_t reset_address = *(volatile uint32_t*) (0x08008000U +4) ;
	
	app_reset_handler =(void*)reset_address;  ////point user flash address with reset address
	
	app_reset_handler();  //run user flash address with reset address
	
}

